package week7_Homework;

public class Task22 {
/*
        22. Write java program using for loop and print your name 10 times.
 */

    //Main method
    public static void main(String[] args) {
        //for loop
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + ". " + "ANISH"); //print ANISH in every loop
        }
    }
}
