package week7_Homework;

public class Task23 {
/*
        23. Write java program using for loop and print surname 9 times.
 */

    //Main method
    public static void main(String[] args) {
        //for loop
        for (int i = 1; i <= 9; i++) {
            System.out.println(i + ". " + "VEKARIA"); //print VEKARIA in every loop
        }
    }
}
