package week7_Homework2;

public class Task3 {
/*
        3. Write an infinite loop. An infinite loop never ends. Condition is always true.
 */

    //Main method
    public static void main(String[] args) {
        for (int i = 0; i < 2; i--) { //loop never ends as the condition is always true
            System.out.println("Hello JAVA!"); //print statement in the loop
        }
    }
}
